def build_sample_corpus(include_pdf_docs: bool | None = None) -> list[FinancialDocument]:
    if include_pdf_docs is None:
        config = AppConfig()
        include_pdf_docs = bool(config.llamaparse_api_key)

    return list(_build_sample_corpus_cached(bool(include_pdf_docs)))
